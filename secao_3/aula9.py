#Operadores Aritméticos

#Adição +
adicao = 10 + 10
print('Adição:', adicao)

#Subtração -
subtracao = 10 - 5
print('Subtraçãp:', subtracao)

#Multiplicação *
mult = 10 * 10
print('Multiplicação:', mult)

#Divisão / - float
divisao = 10 / 10
print('Divisão:', divisao)

#Divisão inteira // - int
div_int = 10 // 10
print('Divisão inteira:', div_int)

#Exponenciação ** - Elevado
exponen = 2 ** 10
print('Exponenciação:', exponen)

#Módulo %  - Resto da divsão
mod = 25 % 5
print('Módulo:',mod)
#Exemplo de Módulo
print(10 % 8 == 0) #8 é divisivivel por 10? False
print(16 % 8 == 0) #8 é divisivivel por 16? True